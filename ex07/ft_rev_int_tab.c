#include<unistd.h>

void	ft_rev_tab(int *tab, int size)
{
	int	rev[];
	int	n;
	int	t;

	n = 0;
	t = (size - 1);
	while (n <= t)
	{
		rev[n] = tab[t - n];
		n++;
	}
}
